package variables;

public class FloatingNumberVariable {
	
	public static void main(String[] args) {
		
		//실수 (float, double)
		float floatNumber = 3.0f;
		float floatNumber1 = 3;
		float floatNumber2 = 3.12345678901234567890f;
		double doubleNumber3 = 3.12345678901234567890;
	
		System.out.println(floatNumber); // 3.0
		System.out.println(floatNumber1); // 3.0
		System.out.println(floatNumber2); // 3.1234567
		System.out.println(doubleNumber3);// 3.1234567890123457
		
	
	}

}
